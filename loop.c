#include<stdio.h>

int main()
{
	int i;
	printf("For Loop");
	for(i = 1;i<=10;i++)
	{
		printf("\nTops Technologies");
		
	}
	
	printf("\n\nWhile Loop");
	i = 1;
	while(i <= 10)
	{
		printf("\nTops Technologies");
		i++;
	}
	
	printf("\n\n Do While Loop");
	i = 11;
	do
	{
		printf("\nTops Technologies");	
	}while(i <= 10);
	
	
	return 0;
}
